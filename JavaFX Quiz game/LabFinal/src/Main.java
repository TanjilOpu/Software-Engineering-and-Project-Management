    // ---------------- Student ID Scene ----------------
    private void showStudentIdScene(Stage stage) {
        TextField idField = new TextField();
        idField.setPromptText("Enter Student ID");

        Button startBtn = new Button("Start Quiz");

        VBox startBox = new VBox(10, new Label("Enter Student ID"), idField, startBtn);
        startBox.setPadding(new Insets(20));
        Scene startScene = new Scene(startBox, 350, 200);

        startBtn.setOnAction(e -> {
            studentId = idField.getText().trim();

            if (!validateStudent(studentId)) {
                showAlert("❌ Invalid Student ID");
                return;
            }

            char lastChar = studentId.charAt(studentId.length() - 1);
            int lastDigit = Character.getNumericValue(lastChar);

            String[][] selectedQuestions = getQuestionsByRollDigit(lastDigit);
            quizController = new QuizController(selectedQuestions);

            stage.setScene(quizScene(stage));
            quizController.loadQuestion(questionLabel, options, group);
        });

        stage.setTitle("High School Quiz");
        stage.setScene(startScene);
        stage.show();
    }

    // ---------------- Quiz Scene ----------------
    private Scene quizScene(Stage stage) {
        VBox box = new VBox(10);
        box.setPadding(new Insets(20));

        for (int i = 0; i < 4; i++) {
            options[i] = new RadioButton();
        }

        nextBtn.setOnAction(e -> {
            boolean hasNext = quizController.checkAnswer(options);
            if (hasNext) {
                quizController.loadQuestion(questionLabel, options, group);
            } else {
                saveResult();
                showPlayAgainAlert(stage);
            }
        });

        box.getChildren().addAll(questionLabel, options[0], options[1], options[2], options[3], nextBtn);
        return new Scene(box, 450, 300);
    }

    // ---------------- Show alert with Play Again option ----------------
    private void showPlayAgainAlert(Stage stage) {
        Alert alert = new Alert(Alert.AlertType.CONFIRMATION);
        alert.setTitle("Quiz Finished");
        alert.setHeaderText("Your Score: " + quizController.getScore());
        alert.setContentText("Do you want to play again?");

        ButtonType yesBtn = new ButtonType("Yes");
        ButtonType noBtn = new ButtonType("No");

        alert.getButtonTypes().setAll(yesBtn, noBtn);

        alert.showAndWait().ifPresent(type -> {
            if (type == yesBtn) {
                showStudentIdScene(stage); // go back to ID input screen
            } else {
                stage.close(); // exit
            }
        });
    }

    // ---------------- Validate Student ----------------
    private boolean validateStudent(String id) {
        try {
            Connection con = DBConnection.getConnection();
            if (con == null) {
                showAlert("Database connection failed");
                return false;
            }

            PreparedStatement ps = con.prepareStatement("SELECT * FROM student WHERE student_id = ?");
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();

            return rs.next();
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }

    // ---------------- Save result ----------------
    private void saveResult() {
        try {
            Connection con = DBConnection.getConnection();
            PreparedStatement ps = con.prepareStatement("INSERT INTO result (student_id, score) VALUES (?, ?)");
            ps.setString(1, studentId);
            ps.setInt(2, quizController.getScore());
            ps.executeUpdate();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // ---------------- Show simple alert ----------------
    private void showAlert(String msg) {
        Alert alert = new Alert(Alert.AlertType.INFORMATION);
        alert.setContentText(msg);
        alert.showAndWait();
    }

